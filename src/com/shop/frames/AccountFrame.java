    public void fillTable() {

        
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        List<Account> allAccounts = accountDAOImpl.getAllAccount();

        for (Account a : allAccounts) {

            Object rowData[] = {a.getAccountID(), a.getAccountName(),a.getBankName(),a.getAccountNumber(),a.getCurrentBalance(),a.getCreatedDate(),a.getCreatedBy(),a.getModifiedDate(),a.getModifiedBy()};

            model.addRow(rowData);
            accountTable.setModel(model);

        }
    }
    
