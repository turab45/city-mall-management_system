    public String generateAccountNumber(){
        Random rd = new Random();
        String accNo = "";
        String confirmedAccount = null;
        for(int i=1; i<=12; i++){
        
            accNo+=Integer.toString(rd.nextInt(9));
        }
        if(!accountDAOImpl.checkAccountNumber(accNo)){
        
            confirmedAccount = accNo;
        }else{
        
            generateAccountNumber();
        }
        
        return confirmedAccount;
    }

