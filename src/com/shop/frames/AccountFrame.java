    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        if(nameField.getText().equals("") || bankField.getText().equals("") || initialBalanceField.getText().equals("")){
        
                            JOptionPane.showMessageDialog(null, "No Account Detail's found!", "Error", JOptionPane.WARNING_MESSAGE);

        }else{
        
            String name = nameField.getText();
            String bank = bankField.getText();
            double initialBalance = Double.valueOf(initialBalanceField.getText());
            
            Account account = new Account();
            account.setAccountName(name);
            account.setAccountNumber(generateAccountNumber());
            account.setBankName(bank);
            account.setCurrentBalance(initialBalance);
            account.setCreatedBy(LoginFrame.getUser().getRole().getRoleId());
            account.setCreatedDate(new Date());
            
            int result = accountDAOImpl.addAccount(account);
            if (result > 0){
                JOptionPane.showMessageDialog(null, "Account has been created successfully!");
                fillTable();
            }
            else
                JOptionPane.showMessageDialog(null, "Error creating account!", "Error", JOptionPane.ERROR);
        }
        
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if(nameField.getText().equals("") || bankField.getText().equals("") || initialBalanceField.getText().equals("")){
        
                            JOptionPane.showMessageDialog(null, "Select account to update!", "Error", JOptionPane.WARNING_MESSAGE);

        }else{
        
            String name = nameField.getText();
            String bank = bankField.getText();
            double initialBalance = Double.valueOf(initialBalanceField.getText());
            
            Account account = accountDAOImpl.getAccountByID(id);
            account.setAccountName(name);
            account.setAccountNumber(generateAccountNumber());
            account.setBankName(bank);
            account.setCurrentBalance(initialBalance);
            account.setModifiedBy(LoginFrame.getUser().getRole().getRoleId());
            account.setModifiedDate(new Date());
            
            int result = accountDAOImpl.updateAccount(account);
            if (result > 0){
                JOptionPane.showMessageDialog(null, "Account has been updated successfully!");
                fillTable();
            }
            else
                JOptionPane.showMessageDialog(null, "Error updating account!", "Error", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

