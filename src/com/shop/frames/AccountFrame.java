    private void accountTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_accountTableMouseClicked
        id = (Integer) accountTable.getValueAt(accountTable.getSelectedRow(), 0);

        if (id != null) {
            
            updateButton.setEnabled(true);
            deleteButton.setEnabled(true);
            
            account = accountDAOImpl.getAccountByID(id);

            nameField.setText(account.getAccountName());
            bankField.setText(account.getBankName());
            initialBalanceField.setText(account.getCurrentBalance().toString());
            
            updateButton.setEnabled(true);
            deleteButton.setEnabled(true);
        }
    }//GEN-LAST:event_accountTableMouseClicked

