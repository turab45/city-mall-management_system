    public void fillTable() {

        Object columns[] = {"ID", "Product","Category","Quantity","Price", "Created Date", "Created By", "Modified Date", "Modified By"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        List<Product> allProducts = productDAOImpl.getAllProduct();
        
        for (Product p : allProducts) {
            Category c = categoryDAOImpl.getCategoryByID(p.getCategory().getCategoryID());
            Object rowData[] = {p.getProductID(), p.getProductName(),c.getCategoryName(),p.getQuantity(),p.getPrice(),p.getCreatedDate(),p.getCreatedBy(),p.getModifiedDate(),p.getModifiedBy()};

            model.addRow(rowData);
            productTable.setModel(model);

        }
    }
    
