    public void fillCategoryCombo(){
    
        List<Category> allCategories = categoryDAOImpl.getAllCategory();
        categoryCombo.addItem("Select Category");
        for(Category c : allCategories){
        
            categoryCombo.addItem(c.getCategoryName());
        }
    }

