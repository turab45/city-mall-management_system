    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
       Integer result = null;
        if (nameField.getText().equals("") || quantityField.getText().equals("") || priceField.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Provide all required information to add a new account!", "Please fill the field!", 0);

        } else {

            String name = nameField.getText().toString();
            Integer quantity = Integer.valueOf(quantityField.getText());
            Double price = Double.valueOf(priceField.getText().toString());
            
            Product p = new Product();
            Category c = categoryDAOImpl.getCategoryByID(categoryDAOImpl.getCategoryIDByName(categoryCombo.getSelectedItem().toString()));
            p.setProductName(name);
            p.setCategory(c);
            p.setQuantity(quantity);
            p.setPrice(price);
            p.setCreatedDate(new Date());
            p.setCreatedBy(0);
            
            
             result = productDAOImpl.addProduct(p);
             
             if (result > 0) {
                JOptionPane.showMessageDialog(null, p.getProductName() + " added successfully!");
                clearFields();
                fillTable();
            } else {
                JOptionPane.showMessageDialog(null, "ERROR", "Error", JOptionPane.ERROR_MESSAGE);

            }

            
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Product p = productDAOImpl.getProductByID(id);
        Product pr = p;
        int selection = JOptionPane.showConfirmDialog(null, "Do you want to update Product: " + pr.getProductName() + "?", null, 0);
        if (selection == 0) {
            
            Category c = categoryDAOImpl.getCategoryByID(categoryDAOImpl.getCategoryIDByName(categoryCombo.getSelectedItem().toString()));
            
            p.setProductName(nameField.getText());
            p.setCategory(c);
            p.setQuantity(Integer.valueOf(quantityField.getText()));
            p.setPrice(Double.valueOf(priceField.getText()));
            p.setModifiedDate(new Date());
            p.setModifiedBy(0);
            
            Integer result = productDAOImpl.updateProduct(p);
            if(result>0){
            JOptionPane.showMessageDialog(null, pr.getProductName() + " Updated Successfully!");
            clearFields();
            fillTable();
            }else {

            JOptionPane.showMessageDialog(null, "Error in update", "Error", JOptionPane.ERROR_MESSAGE);

        }
        } 
    }//GEN-LAST:event_updateButtonActionPerformed

    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        id = (Integer) productTable.getValueAt(productTable.getSelectedRow(), 0);

        if (id != null) {
            
            updateButton.setEnabled(true);
            deleteButton.setEnabled(true);
            
            product = productDAOImpl.getProductByID(id);
            Category c = categoryDAOImpl.getCategoryByID(product.getCategory().getCategoryID());
            nameField.setText(product.getProductName());
            quantityField.setText(product.getQuantity().toString());
            priceField.setText(product.getPrice().toString());
            categoryCombo.setSelectedItem(""+c.getCategoryName());
            
            updateButton.setEnabled(true);
            deleteButton.setEnabled(true);
        }
    }//GEN-LAST:event_productTableMouseClicked

